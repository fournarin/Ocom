#include<bits/stdc++.h>
using namespace std;
int idi[]={-1,-1, 0, 1, 1, 1, 0,-1,0};
int idj[]={ 0, 1, 1, 1, 0,-1,-1,-1,0};
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    int n;cin>>n;int sum=0;
    int a[n+2][n+2]={};
    int x,y;cin>>y>>x;
    for(int i=1;i<n+1;i++){
        for(int j=1;j<n+1;j++){
            char d;cin>>d;
            if(d=='*') for(int k=0;k<9;k++) a[i+idi[k]][j+idj[k]]++;
            a[0][j]=-100;
            a[n+1][j]=-100;
            a[i][0]=-100;
            a[i][n+1]=-100;}}
    a[x][y]=-1;
    bool chk=true;
    while(chk){
        chk=false;
    for(int i=1;i<n+1;i++)
        for(int j=1;j<n+1;j++)
            if(a[i][j]==-1) {
                for(int k=0;k<9;k++) 
                if(a[i+idi[k]][j+idj[k]]==0){
                a[i+idi[k]][j+idj[k]]=-1;
                chk=true;}}}
    for(int i=1;i<n+1;i++){
        for(int j=1;j<n+1;j++){
            if(a[i][j]==-1) {
                for(int k=0;k<9;k++) 
                if(a[i+idi[k]][j+idj[k]]>0){
                sum+=a[i+idi[k]][j+idj[k]];
                a[i+idi[k]][j+idj[k]]=-100;
            }
            }
        }
    }
    cout<<sum;
    return 0;
}
