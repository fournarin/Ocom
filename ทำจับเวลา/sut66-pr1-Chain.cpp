using namespace std;
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    string s;cin>>s;int n;cin>>n;int l=s.length();
    for(int i=0;i<n+n+1;i++){
        for(int j=0;j<n+n;j++){
            if(i==n&&j==n+n-1) cout<<s[1];
            else if(i+j==n||i+j==3*n||i-j==n||j-i==n) cout<<s[0];
            else cout<<".";
        }
        for(int x=1;x<l-1;x++){
        for(int j=1;j<n+n;j++){
            if(i==n&&j==1) cout<<s[x-1];
            else if(i==n&&j==n+n-1) cout<<s[x+1];
            else if(i+j==n||i+j==3*n||i-j==n||j-i==n) cout<<s[x];
            else cout<<".";
        }}
        for(int j=1;j<n+n+1;j++){
            if(i==n&&j==1) cout<<s[l-2];
            else if(i+j==n||i+j==3*n||i-j==n||j-i==n) cout<<s[l-1];
            else cout<<".";
        }
        cout<<"\n";
    }
    return 0;
}
