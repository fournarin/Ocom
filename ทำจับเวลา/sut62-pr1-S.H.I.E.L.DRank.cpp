#include<bits/stdc++.h>
using namespace std;
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    char c; cin>>c;
    int n,m; cin>>n>>m;
    for(int x=0;x<m;x++){
    for(int i=0;i<n+n+1;i++){
        for(int y=0;y<m;y++){
        for(int j=0;j<n+n+1;j++){
            if(x==y&&i==j) cout<<"#";
            else if(i+j==n+n&&x+y==m-1) cout<<"#";
            else if(i==j||i==n||j==n||i+j==n+n) cout<<"+";
            else if(i+j<=3*n&&i+j>=n&&i-j<=n&&j-i<=n) cout<<c;
            else cout<<".";
        }}
        cout<<"\n";
    }}
    return 0;
}
